    private void importCSV2() {
    DefaultTableModel model = (DefaultTableModel) tbl_Emp2.getModel();
    model.setRowCount(0); // Clear existing data

    String filePath = "C:\\Users\\IT-Spare\\Documents\\NetBeansProjects\\MotorPH2_v2_Ryu\\src\\motorph2_v2\\MotorPH Employee Details.csv";

    try (BufferedReader br = new BufferedReader(new FileReader(filePath))) {
        String line;
        while ((line = br.readLine()) != null) {
            String[] columns = line.split(",");
            
            // Check if the columns array has enough elements
            if (columns.length >= 25) {
                // Add only specific columns to the table model
                model.addRow(new Object[]{
                    columns[0],  // Emp No.
                    columns[1],  // Last Name
                    columns[2],  // First Name
                    columns[10], // Status
                    columns[11], // Position
                    columns[12], // Supervisor
                    columns[19], // SLCount
                    columns[20], // VLCount
                    columns[21], // SLUsed
                    columns[22], // VLUsed
                    columns[23], // SLBal
                    columns[24]  // VLBal
                });
            } else {
                System.out.println("Invalid line format - Length: " + columns.length + ", Line: " + line);
            }
        }
    } catch (IOException ex) {
        JOptionPane.showMessageDialog(this, "Error reading CSV file: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }


}
